/**
 * 家具名称索引 Map (O(1) 查询)
 */
export const KJL_FURNITURE_SET = new Set(KJL_FURNITURE_NAMES);

/**
 * 快速检查是否为有效家具
 */
export function isValidFurniture(name: string): boolean {
  return KJL_FURNITURE_SET.has(name);
}

/**
 * 模糊匹配家具名称
 */
export function findFurniture(text: string): string | null {
  // 完全匹配
  if (KJL_FURNITURE_SET.has(text)) {
    return text;
  }

  // 包含匹配（优先长名称）
  for (const name of KJL_FURNITURE_NAMES) {
    if (text.includes(name)) {
      return name;
    }
  }

  return null;
}

/**
 * 从文本中提取所有家具
 */
export function extractAllFurniture(text: string): string[] {
  const found = new Set<string>();

  for (const name of KJL_FURNITURE_NAMES) {
    if (text.includes(name)) {
      found.add(name);
    }
  }

  return Array.from(found);
}
